package arrays;

public class OneDimensionalArray {

	public static void main(String[] args) {
		// int[] a= new int[3];
		int a[] = new int[3];
		a[0] = 2;
		a[1] = 5;
		a[2] = 7;
		

		for (int i = 0; i < 3; i++) {

			System.out.println(a[i]);
		}

		int[] b = { 1, 3, 6 };
		for (int i = 0; i < 3; i++) {

			System.out.println(b[i]);
		}

	}

}
